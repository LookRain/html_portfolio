document.addEventListener('DOMContentLoaded', function () {
  particleground(document.getElementById('particles'), {
    dotColor: '#65a3d8',
    lineColor: '#65a3d8'
  });
  var intro = document.getElementById('wrapper');
  intro.style.marginTop = - intro.offsetHeight  + 'px';
}, false);


